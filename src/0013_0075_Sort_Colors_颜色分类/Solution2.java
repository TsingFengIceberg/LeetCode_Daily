public class Solution2 {
    /**
     * 进阶 O(N) 一趟扫描法：荷兰国旗问题 (双端向中间挤压)
     */
    public void sortColors(int[] nums) {
        // l 表示 0 的右边界，初始为 -1 说明当前还没有 0
        // r 表示 2 的左边界，初始为 nums.length 说明当前还没有 2
        // i 是当前的探索游标
        int l = -1, r = nums.length, i = 0;
        int temp; // 交换用的临时变量
        
        // 当游标 i 撞到 2 的左边界时，说明未知的盲盒区已经探索完毕
        while (i < r) {
            if (nums[i] == 0) {
                // 遇到 0，必须把它放到左边去
                temp = nums[i];
                // 和 0 区边界的下一个元素（必然是 1）交换
                nums[i] = nums[l + 1];
                nums[l + 1] = temp;
                
                l++; // 0 区向右扩张
                i++; // 换过来的一定是 1，已经处理妥当，可以放心前进
            } else if (nums[i] == 2) {
                // 遇到 2，必须把它扔到右边去
                temp = nums[i];
                // 从右边盲盒区末尾（r-1）拿一个未知元素过来
                nums[i] = nums[r - 1];
                nums[r - 1] = temp;
                
                r--; // 2 区向左扩张
                // 【绝对不能 i++】：因为换到 i 位置的是右边的盲盒数字，下一轮必须原地检验它！
            } else {
                // 遇到 1，它本来就该待在中间
                i++; // 直接放过，继续向前探索
            }
        }
    }
}


// 为什么遇到 2 时，i 绝对不能加 1？
// 在你的代码中，数组被这三个指针（l, i, r）严格划分成了四个区域。
// 哪怕是在循环的任意一刻，这四个区域的物理意义也是绝对不变的（这就是“循环不变量”）：

// 区域 A [0, l]：全都是确认过的 0。

// 区域 B [l+1, i-1]：全都是确认过的 1。（注意，i 左边的所有东西，都是你已经“走过并看过的”）。

// 区域 C [i, r-1]：未知探索区！盲盒区！ 这里面的数字可能是 0、1 或 2，你根本不知道。

// 区域 D [r, nums.length - 1]：全都是确认过的 2。

// 现在我们来看看交换逻辑的本质区别：

// 情况 1：当 nums[i] == 0 时，为什么 i 可以放心 ++？
// 当你发现当前位置是 0 时，你要把它和 nums[l+1] 交换。
// 重点来了：l+1 这个位置在哪？它在区域 B（全都是确认过的 1）的边界上！
// 也就是说，被换到 i 位置上来的那个元素，百分之百是一个 1（或者就是 i 本身）。既然换过来的数字必定是 1，而 1 本来就应该待在中间，说明这个位置的数据已经彻底处理完毕了。所以探索者 i 可以安心地往前走一步（i++）。

// 情况 2：当 nums[i] == 2 时，为什么 i 绝对不能 ++？
// 当你发现当前位置是 2 时，你要把它和 nums[r-1] 交换。
// 致命的重点来了：r-1 这个位置在哪？它在**区域 C（未知盲盒区）**的最右端！
// 你把一个右边的“盲盒”强行塞到了 i 这个位置。这个盲盒里装的可能是一个 1，但也可能是一个 0，甚至还是一个 2！
// 如果你这个时候执行了 i++，探索者就会直接跨过这个盲盒，导致这个从右边换过来的未知数字被永远地遗留在了“确认过的 1”的区域里，整个排序就彻底毁了。
// 所以，当从右边拿过来一个未知数字时，i 必须停留在原地，在下一轮循环中，亲自“开盲盒”检查这个新换过来的数字到底是个啥。