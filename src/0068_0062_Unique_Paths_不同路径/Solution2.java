class Solution2 {
    // 【大厂满分级 Code Review】
    // 时间复杂度: O(m * n)，需要遍历整个网格。
    // 空间复杂度: O(n)，将二维矩阵极致压缩成了长度为 n 的一维滚动数组！
    public int uniquePaths(int m, int n) {
        // 只需要维护一行的状态
        int[] dp = new int[n];
        
        // 【状态初始化】
        // 第一行的所有格子，因为只能一直向右走，所以路径数全是 1
        for (int j = 0; j < n; j++) {
            dp[j] = 1;
        }
        
        // 从第二行（i = 1）开始往下滚
        for (int i = 1; i < m; i++) {
            // 每行的第 0 列因为只能一直向下走，永远是 1，所以 j 从 1 开始即可
            for (int j = 1; j < n; j++) {
                // 核心状态压缩方程！
                // 这里的 dp[j] 在更新前，代表的是正上方（上一行）的值
                // 这里的 dp[j-1] 在这一层循环刚刚被更新过，代表的是左边的值
                dp[j] = dp[j] + dp[j - 1];
            }
        }
        
        // 返回数组的最后一个元素，即到达右下角的总路径数
        return dp[n - 1];
    }
}